func calculate_Fib(num: Int) -> Int {
    print("Calculating fibonacci(\(num))...")
    if num <= 2 {
        print("Terminal case, returning 1.")
        return 1
    } else {
        let fib = calculate_Fib(num: num - 1) + calculate_Fib(num: num - 2)
        print("Non-terminal case, returning \(fib).")
        return fib
    }
}

print("The final result is: \(calculate_Fib(num: n)).")
