func calculate_Factorial(number : Int) -> Int {
    //segmentation fault if the number reaches 21
    precondition(n < 21, "Segmentation Fault")
    
    print("Calculating factorial(\(number))...")
    if number <= 1 {
        print("Terminal case, returning 1.")
        return 1                                          
    } else {        
        let result = number * calculate_Factorial(number: number - 1)      
        print("Non-terminal case, returning \(result).")
        return result
    }
}

print("The final result is: \(calculate_Factorial(number: n)).")
